recipeDescription =
`Zuerst die Paprika und Zucchini in kleine Würfel schneiden(ca. 1cm groß), 
den Cheddar grob reiben und den Mais und die Kidneybohnen unter kaltem Wasser spülen. 
Die Gewürze in einer kleinen Tasse zusammenmischen. Dann eine Pfanne mit Öl erhitzen 
und die Zucchini und Paprika scharf anbraten. Die Gewürzmischung hinzugeben,
vermischen und die Pfanne von der heißen Platte stellen. Sofort die Mais-Bohnen-Mischung 
hinzugeben, kurz in der nocht leicht heißen Pfanne mit anbraten und dann die gesamte 
Mischung durch ein Nudelsieb abgießen. Während die Gemüsemischung noch abtropft, 
ein wenig Öl in der Pfanne erhitzen und auf die kleinste Stufe stellen. 
Einen Weizentortilla mit Käse bestreuen, ein Drittel Gemüsemischung darauf verteilen 
und mit Käse toppen. Den Tortilla in die Pfanne legen, einen zweiten oben drauf und 
sobald der Käse leicht geschmolzen ist, einmal wenden, kurz liegen lassen und dann 
herausnehmen.`;

