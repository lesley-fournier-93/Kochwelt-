recipeDescription =
`1.
Kartoffeln ordentlich waschen und gut abtropfen lassen. Dann in eine große Schüssel geben, mit gutem Olivenöl beträufeln, salzen, ein wenig vom Rosmarin klein schneiden und darüber streuen. Dann bei 220 bis 250 Grad für ca. 45 min. in den vorgeheizten Ofen geben.<br>
<br>
2.
Die Entenbrust abwaschen, trocken tupfen und die Haut einritzen (Nur die Haut einritzen, nicht das Fleisch!). Ente (mit der Hautseite), Knoblauch, Zwiebeln und einen Rosmarinzweig in die Pfanne geben und bei guter Hitze anbraten, bis die Haut eine leckere, braune Farbe hat. Danach wenden und auch die zweite Seite gut anbraten. Danach die Ente salzen und pfeffern. Du kannst hier auch Deine Lieblingsgewürzmischung dazu geben. Danach entweder in der Pfanne (prüfen, ob sie ofenfest ist) oder in einer Auflaufform für ca. 15 min. in den vorgeheizten Ofen geben. Die Ente sollte im Kern rosa sein, wenn sie auf den Tisch kommt.<br>
<br>
3.
Danach alles aus dem Ofen nehmen und anrichten… fertig. Guten Appetit!<br>
<br>
<b>Tipp:&ensp;</b>Als weitere Beilage eignet sich eine leckere Portion Rotkohl.`;

