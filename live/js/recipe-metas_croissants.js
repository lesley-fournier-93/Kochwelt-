recipeDescription = `
Alle Zutaten in die Schüssel einer Rührmaschine geben und für etwa fünf
bis zehn Minuten langsam kneten. Der Hefeteig sollte nach dem Kneten möglichst geschmeidig und dehnbar sein,
dabei aber nicht mehr kleben. Den Teig über Nacht im Kühlschrank ruhen lassen.
<br><br>
125 g Butter | 15 g Weizenmehl Type 550
Die weiche Butter mit dem Mehl verkneten. Die Ziehbutter zu einem Rechteck formen, einpacken und
im Kühlschrank über Nacht lagern.
<br><br>
Den Hefeteig auf rechteckig und auf etwa 0,5cm Dicke ausrollen. Die Butter ebenfalls rechteckig ausrollen, allerdings nur halb so
groß wie den Hefeteig. Die Ziehbutter nun auf den ausgerollten Grundteig legen und den Teig eng um die
Butter schlagen und mit den Fingerspitzen die Enden des Teigs zusammendrücken,
sodass keine Butter mehr zu sehen ist. Den Teig nun für 30 Minuten kalt stellen.
<br><br>
Den Teig nun auf etwa 20x45cm ausrollen. Restliches Mehl mit der Hand oder mit einem Pinsel abkehren.
Nun ein Drittel des Teigs einschlagen und das gegenüberliegende Drittel darüber schlagen.
Den Teig erneut für 30 Minuten kühlen lassen und den Schritt anschließend wiederholen.
Den Schritt zweimal wiederholen, dabei den Teig nach jeder Tour um 90° drehen und jeweils 30 Minuten kühlen.
<br><br>
Die Arbeitsfläche mit Mehl bestauben. Den Teig etwa einen halben Zentimeter dünn ausrollen.
<br><br>
Den Croissantteig mit einem scharfen Messer in 8x15cm große Rechtecke schneiden. Die Rechtecke zu zwei gleich großen Dreiecken halbieren.
<br><br>
Die Croissants von der kurzen Kante zur Spitze auf der gegenüberliegenden Seite hin einrollen.
Hinweis: Die Croissants lassen sich können jetzt eingefroren werden.
<br<<br>
1 Ei
Die Croissants auf ein Backblech setzen und an einem warmen Ort für 30 Minuten gehen lassen. In der Zwischenzeit das Ei mit einer Prise Salz verquirlen.
Den Backofen auf 170°C vorheizen. Die aufgegangenen Croissants vorsichtig mit der Eistreiche bepinseln.
Die Croissants für etwa 15 Minuten backen.
<br>
<br>
<br>
Nun ist alles servierfertig. Guten Appetit.`;
